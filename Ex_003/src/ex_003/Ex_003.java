package ex_003;

/**
 *003)Crie um programa que declare e inicialize variáveis de todos os tipos primitivos
 * em Java (int, double, char, boolean, etc.). Exiba o valor de cada variável no console.
 * @author carol
 */
public class Ex_003 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Variaveis e tipos de Dados
        int idade = 25;
        double altura = 1.75;
        char inicial = 'M';
        boolean estudante = true;
        //Variaveis declaradas nos tipos primitivos em Java e inicialização
        
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Incial do nome: " + inicial);
        System.out.println("Eh estudante? " + estudante);
        
    }
    
}
